package main

import (
	"fmt"
	"strings"
)

func main() {
	cat := [][]string{
		[]string{" ", " ", " ", " ", " ", " ", " ", " ", " ", " ", " "}, // \      /\
		[]string{" ", " ", " ", " ", " ", " ", " ", " ", " ", " ", " "}, //  )    (  ^)
		[]string{" ", " ", " ", " ", " ", " ", " ", " ", " ", " ", " "}, // (   /    )
		[]string{" ", " ", " ", " ", " ", " ", " ", " ", " ", " ", " "}, //  \ ( __ )|
	}

	cat[0][0] = "\\"
	cat[0][7] = "/"
	cat[0][8] = "\\"
	cat[1][1] = ")"
	cat[1][6] = "("
	cat[1][9] = "^"
	cat[1][10] = ")"
	cat[2][0] = "("
	cat[2][4] = "/"
	cat[2][9] = ")"
	cat[3][1] = "\\"
	cat[3][3] = "("
	cat[3][5] = "_"
	cat[3][6] = "_"
	cat[3][8] = ")"
	cat[3][9] = "|"

	for i := 0; i < len(cat); i++ {
		fmt.Println(strings.Join(cat[i], ""))
	}
}
